    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        String name="", email="", phoneNo="", username="", password="", rePassword="";
        Integer type=1;
        try 
        {
            name = regNameText.getText();
            email = regEmailText.getText();
            phoneNo = regPhoneText.getText();
            username = regUserText.getText();
            password = regPassText.getText();
            rePassword = regRePassText.getText();

        }
        catch (NullPointerException e)
        {
            JOptionPane.showMessageDialog(this,"Fill up all box", "Wrong Entry !!",JOptionPane.INFORMATION_MESSAGE);
        }

        if(regAdminRad.isSelected()==true) type=1;
        else if(regClientRad.isSelected()==true) type =2;

        if(regAdminRad.isSelected()==false && regClientRad.isSelected()==false)
        {
            JOptionPane.showMessageDialog(this, "Select Admin or Client !!","WARNING !!",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(password.equals(rePassword)==false)
        {
            JOptionPane.showMessageDialog(this, "Password Mismatch !!","WARNING !!",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(name.length()==0 || email.length()==0 || phoneNo.length()==0 || username.length()==0 || password.length()==0 || rePassword.length()==0)
        {
            JOptionPane.showMessageDialog(this, "Fill up all box","Wrong Entry !!",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            String sql = "Select * from users where username like '"+username+"'";
            if(DatabaseHelper.find(sql)==true)
            {
                JOptionPane.showMessageDialog(this, "Same ID exists!!","WARNING !!",JOptionPane.INFORMATION_MESSAGE);
                regUserText.setText("");
                regPassText.setText("");
                regRePassText.setText("");
            }
            else
            {
                sql = "Insert into users values (null,'"+type+"','"+name+"','"+email+"','"+phoneNo+"','"+username+"','"+password+"')";
                //sql = "Insert into users valuse (null,sd,sd,21,sd,123)";
                if(DatabaseHelper.insert(sql)==true)
                {
                    JOptionPane.showMessageDialog(this, "You Are Registered !!", "Registration", JOptionPane.INFORMATION_MESSAGE);
                    regNameText.setText("");
                    regEmailText.setText("");
                    regPhoneText.setText("");
                    regUserText.setText("");
                    regPassText.setText("");
                    regRePassText.setText("");
                    LogInRegister ob = new LogInRegister();
                    ob.setVisible(true);
                    this.setVisible(false);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Check Your Connection", "Try Again", JOptionPane.INFORMATION_MESSAGE);
                    regPassText.setText("");
                    regRePassText.setText("");
                }
            }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

