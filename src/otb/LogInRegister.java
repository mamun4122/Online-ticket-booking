    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String usernameString=logInUser.getText();
        String passwordString=logInPassword.getText();
        Integer type = 1;
        if(adminRadioButton.isSelected()==true) type =1;
        else if(clientRadioButton.isSelected()==true) type =2;

        if(usernameString.length()==0 || passwordString.length()==0 || type==0)
        {
            JOptionPane.showMessageDialog(this, "Correct the Input .","WRONG ENTRY !!",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            String sql = "Select * from users where type like '"+type+"' and username like '"+usernameString+"' and password like '"+passwordString+"'";
            if(DatabaseHelper.find(sql)==true)
            {
                if(type==2)
                {
                    MainFrame ob = new MainFrame();
                    ob.setVisible(true);
                    ob.setUserNameFromLogIn(usernameString);
                    this.setVisible(false);
                }
                else
                {
                    AdminPanel ob = new AdminPanel();
                    ob.setVisible(true);
                    this.setVisible(false);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Invalid Credentials","WRONG ENTRY",JOptionPane.INFORMATION_MESSAGE);
                logInPassword.setText("");
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

